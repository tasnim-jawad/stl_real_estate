<script>
import { onMounted, onUpdated, nextTick, ref, onBeforeUnmount } from "vue";

export default {
  setup() {
    const swiperInstance = ref(null);
    const initializationAttempts = ref(0);
    const maxAttempts = 10;

    const initializeBlogSlider = () => {
      const blogSlider = document.querySelector("#blogSlider1");

      // If slider already exists, don't reinitialize
      if (swiperInstance.value) {
        return;
      }

      if (!blogSlider) {
        console.warn("Blog slider element not found");
        return;
      }

      if (!window.Swiper) {
        initializationAttempts.value++;
        if (initializationAttempts.value < maxAttempts) {
          setTimeout(() => initializeBlogSlider(), 200);
        } else {
          console.error("Swiper library not available after multiple attempts");
        }
        return;
      }

      try {
        // Parse data-slider-options if present
        let options = {};
        const optionsAttr = blogSlider.getAttribute("data-slider-options");
        if (optionsAttr) {
          options = JSON.parse(optionsAttr);
        }

        // Normalize string values to proper types
        const normalizeOptions = (obj) => {
          for (const key in obj) {
            if (typeof obj[key] === "string") {
              if (obj[key] === "true") obj[key] = true;
              else if (obj[key] === "false") obj[key] = false;
              else if (!isNaN(obj[key]) && obj[key] !== "")
                obj[key] = Number(obj[key]);
            } else if (typeof obj[key] === "object" && obj[key] !== null) {
              normalizeOptions(obj[key]);
            }
          }
        };
        normalizeOptions(options);

        // Add navigation
        const nextBtn = document.querySelector(
          '[data-slider-next="#blogSlider1"]'
        );
        const prevBtn = document.querySelector(
          '[data-slider-prev="#blogSlider1"]'
        );
        if (nextBtn && prevBtn) {
          options.navigation = {
            nextEl: nextBtn,
            prevEl: prevBtn,
          };
        }

        // Initialize slider
        swiperInstance.value = new window.Swiper(blogSlider, {
          autoplay: {
            delay: 4000,
            disableOnInteraction: false,
          },
          autoHeight: true,
          loop: false,
          spaceBetween: 20,
          ...options,
        });

        console.log("Blog slider initialized successfully");
      } catch (error) {
        console.error("Error initializing blog slider:", error);
        // Retry on error
        initializationAttempts.value++;
        if (initializationAttempts.value < maxAttempts) {
          setTimeout(() => initializeBlogSlider(), 500);
        }
      }
    };

    const destroySlider = () => {
      if (
        swiperInstance.value &&
        typeof swiperInstance.value.destroy === "function"
      ) {
        swiperInstance.value.destroy(true, true);
        swiperInstance.value = null;
        console.log("Blog slider destroyed");
      }
    };

    // Initialize on mount
    onMounted(async () => {
      await nextTick();
      setTimeout(() => initializeBlogSlider(), 100);
      setTimeout(() => initializeBlogSlider(), 300);
      setTimeout(() => initializeBlogSlider(), 600);
    });

    // Reinitialize on updates if needed
    onUpdated(async () => {
      await nextTick();
      if (!swiperInstance.value) {
        setTimeout(() => initializeBlogSlider(), 100);
      }
    });

    // Cleanup on unmount
    onBeforeUnmount(() => {
      destroySlider();
    });

    return {
      swiperInstance,
    };
  },
};
</script>

